#include <stdio.h>

int main()
{
  int n,i,j,k=1;
  scanf("%d",&n);
  for(i=1;i<=n*2;i++)
  {
      for(j=1;j<=k;j++)
        printf("*");
      if(i<n)
        k++;
      else
        k--;
      printf("\n");
  }
  return 0;
}
