#include <stdio.h>

void main()
{
int i,j,size,t;

scanf("%d",&size);
t=size/2;

for(i=3;i<=t;i++)
{
  for(j=i;j<t;j++)
  {
      printf(" ");
  }
  for(j=1;j<=(i*2)-1;j++)
  {
      printf("*");
  }
  for(j=1;j<=(2*t-((2*i)-1));j++)
  {
      printf(" ");
  }
  for(j=1;j<=(i*2)-1;j++)
  {
      printf("*");
  }
  printf("\n");
}

for(i=1;i<=size;i++)
{
    for(j=1;j<i;j++)
    {
        printf(" ");
    }
   for(j=1;j<=(2*size-(2*i-1));j++)
   {
       printf("*");
   }
    printf("\n");
}

}
